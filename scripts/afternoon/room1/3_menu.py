# Exercise:  Ordering Pizza
"""
Let's order out for lunch!  
Have each team member add what kind of pizza they'd like from the menu,
then make a dictionary containing the number of each menu item you should tell the
restaurant you all want.

Example:
orders = ["salami", "salami", "vegetarian"]
order_counts = {"salami": 2, "vegetarian": 1, "diablo": 0}
""" 

# Raw Data: 
menu = ["margherita", "salami", "vegetarian", "hawaiian"]
orders = ["margherita", "margherita", "vegetarian", "salami", "vegetarian", "salami", "salami"]  # One item for each pizza

# Script (fill in here):
order_counts = {}
for item in menu:
    item_count = orders.count(item)
    order_counts[item] = item_count

# Output:
print(order_counts)


